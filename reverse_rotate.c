#include "push_swap.h"

void	reverse_rotate(t_stack **stack)
{
	t_stack	*temp;
	t_stack	*cur;

	temp = *stack;
	if ((*stack)->next == NULL || temp == NULL)
		return ;
	while ((temp->next)->next != NULL)
	{
		temp = temp->next;
	}
	cur = temp->next;
	temp->next = NULL;
	cur->next = *stack;
	*stack = cur;
}

void	rra(t_stack **a)
{
	reverse_rotate(a);
	ft_putstr_fd("rra\n", 1);
}

void	rrb(t_stack **b)
{
	reverse_rotate(b);
	ft_putstr_fd("rrb\n", 1);
}

void	rrr(t_stack **a, t_stack **b)
{
	reverse_rotate(a);
	reverse_rotate(b);
	ft_putstr_fd("rrr\n", 1);
}
